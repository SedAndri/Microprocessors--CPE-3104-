PROCED1 SEGMENT 'CODE'
ON_OFF PROC FAR
ASSUME CS:PROCED1, DS:DATA
ORG 00000H
   PUSHF
   PUSH AX
   PUSH DX
   CMP ON_FLAG, 1
   JE RESET_ON
   MOV ON_FLAG, 1
   MOV MODE_FLAG, 1
   JMP EXIT_ON_OFF
   RESET_ON:
      MOV ON_FLAG, 0
      MOV PAUSE_FLAG, 0
   EXIT_ON_OFF:
   POP DX
   POP AX
   POPF
   IRET
ON_OFF ENDP
PROCED1 ENDS

PROCED2 SEGMENT 'CODE'
PAUSE_PLAY PROC FAR
ASSUME CS:PROCED2, DS:DATA
ORG 00050H
   PUSHF
   PUSH AX
   PUSH DX
   CMP ON_FLAG, 1
   JNE EXIT_PAUSE_PLAY
   CMP PAUSE_FLAG, 1
   JE RESET_PAUSE
   MOV PAUSE_FLAG, 1
   JMP EXIT_PAUSE_PLAY
   RESET_PAUSE:
      MOV PAUSE_FLAG, 0
   EXIT_PAUSE_PLAY:
   POP DX
   POP AX
   POPF
   IRET
PAUSE_PLAY ENDP
PROCED2 ENDS

PROCED3 SEGMENT 'CODE'
MODES PROC FAR
ASSUME CS:PROCED3, DS:DATA
ORG 00100H
   PUSHF
   PUSH AX
   PUSH DX
   CMP PAUSE_FLAG, 1
   JNE EXIT_MODE
   INC MODE_FLAG
   MOV PAUSE_FLAG, 0
   CMP MODE_FLAG, 4
   JNE EXIT_MODE
   MOV MODE_FLAG, 1
   EXIT_MODE:
   POP DX
   POP AX
   POPF
   IRET
MODES ENDP
PROCED3 ENDS

