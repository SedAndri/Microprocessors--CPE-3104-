   ; Print character from the specified font
   PRINT_CHAR:
      MOV AH, 11111110B
      MOV DI, SI
      MOV AL, MODE_FLAG
      MOV TEMP, AL
   F1:
      CMP PAUSE_FLAG, 1
      JE PAUSE
      MOV AL, AH
      OUT PORTB, AL
      MOV AL, BYTE PTR CS:[SI]    ; Get row bits
      CALL REV5                   ; FIX: reverse 5 LSBs (mirror correction)
      OUT PORTA, AL
      CMP ON_FLAG, 0
      JE HERE
      CALL DELAY_250MS
      MOV AL, 00H
      OUT PORTA, AL
      INC SI
      CLC
      ROL AH, 1
      CALL DELAY_500MS
      JC F1
   RET

   PAUSE:
      MOV SI, DI
      MOV AH, 11111110B
   F2:
      CMP PAUSE_FLAG, 0
      JE UNPAUSE
      MOV AL, AH
      OUT PORTB, AL
      MOV AL, BYTE PTR CS:[SI]    ; Get row bits
      CALL REV5                   ; FIX: mirror correction also during pause
      OUT PORTA, AL
      CMP ON_FLAG, 0
      JE HERE
      CALL DELAY_250MS
      MOV AL, 00H
      OUT PORTA, AL
      INC SI
      CLC
      ROL AH, 1
      JC F2
      JMP HERE
   UNPAUSE:
      MOV AL, MODE_FLAG
      CMP TEMP, AL
      JNE CHECK_MODE
      RET
      CHECK_MODE:
      CMP MODE_FLAG, 1
      JE DEFAULT
     ; CMP MODE_FLAG, 2
      ;JE STICKMAN
      ;CMP MODE_FLAG, 3
      ;JE LANTERN

   OFF:
      MOV AL, 00000000B
      OUT PORTA, AL
      MOV AL, 11111111B
      OUT PORTB, AL
      MOV ON_FLAG, 0
      MOV MODE_FLAG, 1
   JMP HERE

   DELAY_250MS:	MOV CX, 250
   TIMER1:
      NOP
      NOP
      NOP
      NOP
      LOOP TIMER1
   RET

   DELAY_500MS:	MOV CX, 00AFH	; not 500MS
   L2:
      NOP
      NOP
      LOOP L2
   RET

   DELAY_1MS: MOV BX, 001AH
   L1:
      DEC BX
      NOP
      JNZ L1
      RET
   RET

; --- NEW: reverse 5 LSBs in AL (bit0..bit4) to fix left-right mirroring ---
REV5 PROC NEAR
   PUSH CX
   PUSH DX
  AND AL, 00011111B      ; keep 5 columns
   XOR DL, DL
   MOV CL, 5
@@rev:
   SHR AL, 1              ; take LSB into CF
   RCL DL, 1              ; shift into DL
   LOOP @@rev
   MOV AL, DL
   POP DX
   POP CX
   RET
REV5 ENDP

